texte="Python est genial"

# 1. Afficher la longueur du texte avec len()
print(len(texte))
# 2. Mettre tout en majuscules avec .upper()
print(texte.upper())
# 3. Mettre tout en minuscules avec .lower()
print(texte.lower())
# 4. Remplacer "génial" par "super" avec .replace()
print(texte.replace("genial","super"))
# 5. Séparer les mots avec .split()
print(texte.split())
# 6. Extraire "Python" (slicing) : texte[0:6]
print(texte[0:6])